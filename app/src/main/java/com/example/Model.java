package com.example;

public class Model
{
    private int pic_id;
    private String title_id;


    public Model(int pic_id, String title_id) {
        this.pic_id = pic_id;
        this.title_id = title_id;
    }

    public int getPic_id() {
        return pic_id;
    }

    public String getTitle_id() {
        return title_id;
    }




}
